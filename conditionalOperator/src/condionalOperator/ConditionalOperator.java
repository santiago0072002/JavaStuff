package condionalOperator;

public class ConditionalOperator {

	public static void main(String[] args) {
		int studentGrade = 40; // declaring and initializing
		
	System.out.println(studentGrade >= 60 ? "passed" : "failed"); // this is basically an if statement saying
					// if studentGrade is greateror equal 60 print passed, else represented by the colon: print failed.
	int product = 3; 
	while(product <= 100) { 
		product = product * 3; 
		System.out.println(product);
	}

	}

}
